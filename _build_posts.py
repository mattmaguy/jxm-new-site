def parse_body(raw):
    """Marker-format body text → list of section dicts."""
    sections, current_list = [], None
    def flush_list():
        nonlocal current_list
        if current_list:
            sections.append({"type": "list", "items": current_list})
            current_list = None
    for line in raw.strip().split("\n"):
        line = line.rstrip()
        if not line.strip():
            continue
        if line.startswith("H2::"):
            flush_list()
            sections.append({"type": "h2", "text": line[4:].strip()})
        elif line.startswith("P::"):
            flush_list()
            sections.append({"type": "p", "html": line[3:].strip()})
        elif line.startswith("QUOTE::"):
            flush_list()
            sections.append({"type": "quote", "text": line[7:].strip()})
        elif line.startswith("IMG::"):
            flush_list()
            parts = [p.strip() for p in line[5:].split("|")]
            sections.append({
                "type": "img",
                "src": parts[0],
                "alt": parts[1] if len(parts) > 1 else "",
                "caption": parts[2] if len(parts) > 2 else "",
            })
        elif line.startswith("LIST_ITEM::"):
            if current_list is None:
                current_list = []
            current_list.append(line[11:].strip())
    flush_list()
    return sections


def render_body(sections, slug):
    out = []
    for s in sections:
        t = s["type"]
        if t == "h2":
            out.append(f'            <h2 class="post-h2">{html.escape(s["text"])}</h2>')
        elif t == "p":
            out.append(f'            <p>{s["html"]}</p>')
        elif t == "quote":
            out.append(f'            <blockquote class="post-pullquote"><p>{s["text"]}</p></blockquote>')
        elif t == "img":
            cap = f'<figcaption>{html.escape(s["caption"])}</figcaption>' if s["caption"] else ""
            out.append(
                f'            <figure class="post-figure">'
                f'<img src="../assets/imgs/blog/thoughts/details/{slug}/{s["src"]}" alt="{html.escape(s["alt"])}">'
                f'{cap}</figure>'
            )
        elif t == "list":
            items = "".join(f'<li>{i}</li>' for i in s["items"])
            out.append(f'            <ul class="post-list">{items}</ul>')
    return "\n".join(out)


